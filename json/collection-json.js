function generateclothesBlock(item) {
    let clothessBlock = document.createElement("div");
    clothessBlock.className = "clothes-box";
    let img = document.createElement("img");
    img.src = item.img;
    clothessBlock.appendChild(img);
    clothessBlock.innerHTML += `
                <p class="clothess-block-txt-1">${item.status}</p>
                <p class="clothess-block-txt-2">${item.name}</p>
                <p class="clothess-block-txt-3">${item.info}</p>
                <p class="clothess-block-txt-4">${item.colors}</p>
                <p class="shoes-block-txt-5">$${item.price}</p>
            `;
    return clothessBlock;
}

let wrapper = document.querySelector(".new-featured-clothess-wrapper");
let releaseCnt = document.querySelector(".release-cnt");
let cnt = 0;

function itemDisplay() {
    cnt = 0;
    wrapper.innerHTML = "";
    for (let item of items) {
        wrapper.appendChild(generateclothesBlock(item));
    }
    releaseCnt.innerHTML = items.length;
}

itemDisplay();

function menSelect() {
    cnt = 0;
    wrapper.innerHTML = "";
    for (let item of items) {
        if (item.gender === "man" || item.gender === "unisex") {
            wrapper.appendChild(generateclothesBlock(item));
            cnt++;
        }
    }
    releaseCnt.innerHTML = cnt;
}

